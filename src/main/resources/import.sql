DROP TABLE PRODUCT IF EXISTS;
DROP TABLE CLIENT IF EXISTS;
DROP TABLE CART IF EXISTS;
CREATE TABLE IF NOT EXISTS PRODUCT(ID BIGINT AUTO_INCREMENT, TITLE VARCHAR(255), PRICE NUMBER, PRIMARY KEY (ID));
CREATE TABLE IF NOT EXISTS CLIENT(ID BIGINT AUTO_INCREMENT, NAME VARCHAR(255), PRIMARY KEY (ID));
CREATE TABLE IF NOT EXISTS CART(CLIENT_ID BIGINT, PRODUCT_ID BIGINT, FOREIGN KEY(CLIENT_ID) REFERENCES CLIENT(ID), FOREIGN KEY(PRODUCT_ID) REFERENCES PRODUCT(ID));
